    private void gravarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gravarButtonActionPerformed
        try {
            OutputStream saida = new FileOutputStream("C:\\Users\\Tiago Guerino\\Documents\\NetBeansProjects\\CadastroCliente\\Cliente.txt");
            saida.write(nomeTextField.getText().getBytes());
            saida.write("\r\n".getBytes());
            saida.write(cpfFormattdTextField.getText().getBytes());
            saida.write("\r\n".getBytes());
            saida.write(sexoComboBox.getSelectedItem().toString().getBytes());
            saida.write("\r\n".getBytes());
            saida.write(dataNascimentoFormattedTextField.getText().getBytes());
            saida.write("\r\n".getBytes());
            saida.write(telefoneFormattedTextField.getText().getBytes());
            saida.write("\r\n".getBytes());
            saida.write(telefoneComboBox.getSelectedItem().toString().getBytes());
            saida.write("\r\n".getBytes());
            saida.write(emailTextField.getText().getBytes());
            saida.write("\r\n".getBytes());

            saida.flush();
            saida.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(CadastroClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CadastroClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_gravarButtonActionPerformed

