    private void telefoneComboBoxPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_telefoneComboBoxPopupMenuWillBecomeInvisible
        telefoneFormattedTextField.setText("");
        telefoneFormattedTextField.setFormatterFactory(null);
        telefoneFormattedTextField.setValue(null);
        String texto = telefoneComboBox.getSelectedItem().toString();
        try {
            if (texto.contains("Telefone")) {
                MaskFormatter mascara = new MaskFormatter("(##) ####-####");
                DefaultFormatterFactory formato = new DefaultFormatterFactory(mascara);
                telefoneFormattedTextField.setFormatterFactory(formato);
            } else {
                MaskFormatter mascara = new MaskFormatter("(##) 9####-####");
                DefaultFormatterFactory formato = new DefaultFormatterFactory(mascara);
                telefoneFormattedTextField.setFormatterFactory(formato);
            }
        } catch (ParseException ex) {

        }

    }//GEN-LAST:event_telefoneComboBoxPopupMenuWillBecomeInvisible

    private void carregarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_carregarButtonActionPerformed
        try {
            FileReader leitor = new FileReader("C:\\Users\\Tiago Guerino\\Documents\\NetBeansProjects\\CadastroCliente\\Cliente.txt");
            BufferedReader buffer = new BufferedReader(leitor);
            
            String nome=buffer.readLine();
            String cpf=buffer.readLine();
            String sexo=buffer.readLine();
            String dataNascimento=buffer.readLine();
            String telefone=buffer.readLine();
            String tipoTelefone=buffer.readLine();
            String email=buffer.readLine();
            
            nomeTextField.setText(nome);
            cpfFormattdTextField.setText(cpf);
            dataNascimentoFormattedTextField.setText(dataNascimento);
            telefoneFormattedTextField.setText(telefone);
            emailTextField.setText(email);
            sexoComboBox.setSelectedItem(sexo);
            telefoneComboBox.setSelectedItem(tipoTelefone);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(CadastroClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CadastroClienteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_carregarButtonActionPerformed

