    private void calcularButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularButtonActionPerformed

        if (nomeTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira um nome");
            return;
        }
        if (nota1TextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira a primeira nota");
            return;
        }
        if (nota2TextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira a segunda nota");
            return;
        }
        if (peso1TextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira o peso da primeira nota");
            return;
        }
        if (peso2TextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira o peso da segunda nota");
            return;
        }

        double nota1, nota2, peso1, peso2, media;
        String nota1Str = nota1TextField.getText();
        String nota2Str = nota2TextField.getText();
        String peso1Str = peso1TextField.getText();
        String peso2Str = peso2TextField.getText();

        nota1 = Double.parseDouble(nota1Str.replace(',', '.'));
        nota2 = Double.parseDouble(nota2Str.replace(',', '.'));
        peso1 = Double.parseDouble(peso1Str.replace(',', '.'));
        peso2 = Double.parseDouble(peso2Str.replace(',', '.'));

        if (nota1 > 10 || nota2 > 10 || nota1 <0 || nota2 < 0) {
            JOptionPane.showMessageDialog(this, "Insira uma nota entre 0 e 10");
            return;

        }
        
        if (((int)(peso1 + peso2)) != 1) {
            JOptionPane.showMessageDialog(this, "A soma dos pesos tem que ser igual 1");
            return;
        }
        
      

        media = nota2 * peso2 + nota1 * peso1;
        mediaTextField.setText(String.valueOf(media));

        if (media <= 6) {
            situacaoLabel.setText("Reprovado");
            situacaoLabel.setForeground(Color.red);

        } else {
            situacaoLabel.setText("Aprovado");
            situacaoLabel.setForeground(Color.green);
        }
        

    }//GEN-LAST:event_calcularButtonActionPerformed

