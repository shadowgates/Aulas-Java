    private void maisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_maisButtonActionPerformed

        JFileChooser escolha = new JFileChooser();
        escolha.setDialogTitle("Procurar foto");
        escolha.setFileSelectionMode(JFileChooser.FILES_ONLY);

        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Fotos", "jpg", "png");
        escolha.setFileFilter(filtro);

        int resposta = escolha.showOpenDialog(this);

        if (resposta == JFileChooser.APPROVE_OPTION) {
            File file = escolha.getSelectedFile();
            ImageIcon icone = new ImageIcon(file.getPath());
            Image imagem = icone.getImage();
            Image novoicone = imagem.getScaledInstance(177, 145,  java.awt.Image.SCALE_SMOOTH); // scale it the smooth way  
            icone = new ImageIcon(novoicone);
            imgLabel.setIcon(icone);

        }

    }//GEN-LAST:event_maisButtonActionPerformed

