    private void cadastroTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cadastroTableMouseClicked
        int linhaSelecionada = cadastroTable.getSelectedRow();
        if (linhaSelecionada < 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha");
            return;
        }
        DefaultTableModel tabela;
        tabela = (DefaultTableModel) cadastroTable.getModel();

        Object nomeObj = tabela.getValueAt(linhaSelecionada, 0);
        Object sexoObj = tabela.getValueAt(linhaSelecionada, 1);
        Object rgObj = tabela.getValueAt(linhaSelecionada, 2);
        Object cpfObj = tabela.getValueAt(linhaSelecionada, 3);
        Object telefoneObj = tabela.getValueAt(linhaSelecionada, 4);
        Object celularObj = tabela.getValueAt(linhaSelecionada, 5);
        Object enderecoObj = tabela.getValueAt(linhaSelecionada, 6);
        Object bairroObj = tabela.getValueAt(linhaSelecionada, 7);
        Object estadoObj = tabela.getValueAt(linhaSelecionada, 8);
        Object cidadeObj = tabela.getValueAt(linhaSelecionada, 9);
        Object cepObj = tabela.getValueAt(linhaSelecionada, 10);
        Object emailObj = tabela.getValueAt(linhaSelecionada, 11);
        Object obsObj = tabela.getValueAt(linhaSelecionada, 12);

        nomeTextField.setText(String.valueOf(nomeObj));
        rgFormattedTextField.setText(String.valueOf(rgObj));
        cpfFormattedTextField.setText(String.valueOf(cpfObj));
        enderecoTextField.setText(String.valueOf(enderecoObj));
        cidadeTextField.setText(String.valueOf(cidadeObj));
        bairroTextField.setText(String.valueOf(bairroObj));
        cepFormattedTextField.setText(String.valueOf(cepObj));
        emailTextField.setText(String.valueOf(emailObj));
        celularFormattedTextField.setText(String.valueOf(celularObj));
        obsTextPane.setText(String.valueOf(obsObj));
        telefoneFormattedTextField.setText(String.valueOf(telefoneObj));
        estadoComboBox.setSelectedItem(estadoObj);

        if (sexoObj == "Feminino") {
            femininoRadioButton.setSelected(true);
        } else {
            masculinoRadioButton.setSelected(true);
        }

    }//GEN-LAST:event_cadastroTableMouseClicked

    private void gerarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gerarButtonActionPerformed
        try {
            File arquivo = new File("Contrato - " + nomeTextField.getText() + ".doc");
            FileWriter escritor = new FileWriter(arquivo);
            BufferedWriter buffer = new BufferedWriter(escritor);

            buffer.append("Contrato de Serviço \n\n");
            buffer.append("Eu ");
            buffer.append(nomeTextField.getText());
            buffer.append(", portador do CPF ");
            buffer.append(cpfFormattedTextField.getText());
            buffer.append(", e do RG ");
            buffer.append(rgFormattedTextField.getText());
            buffer.append(" residente no endereço ");
            buffer.append(enderecoTextField.getText());
            buffer.append(" CEP ");
            buffer.append(cepFormattedTextField.getText());
            buffer.append(" bairro ");
            buffer.append(bairroTextField.getText());
            buffer.append(" na cidade ");
            buffer.append(cidadeTextField.getText());
            buffer.append("/");
            buffer.append((String) estadoComboBox.getSelectedItem());
            buffer.append(" estou ciente dos serviços que irei prestar com as observações");
            buffer.append(obsTextPane.getText());
            
            for (int i = 0; i < nomeTextField.getText().length(); i++) {
                 buffer.append("_");
            }

            buffer.append("\n\n");
            buffer.append(nomeTextField.getText());
            buffer.append("\n");
            buffer.append(telefoneFormattedTextField.getText());
            buffer.append("\n");
            buffer.append(emailTextField.getText());
            buffer.append("\n");
            buffer.append(celularFormattedTextField.getText());
            buffer.append("\n");
            
            
            

            buffer.close();
            escritor.close();

        } catch (IOException ex) {
            Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_gerarButtonActionPerformed

