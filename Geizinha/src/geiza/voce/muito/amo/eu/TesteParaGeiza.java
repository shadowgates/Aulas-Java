    private void aceitarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceitarButtonActionPerformed
        if (nomeTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira seu nome por favor");
            return;
        }
        String nome = nomeTextField.getText();
        if (nome.contains("Geiza")) {
            ImageIcon icone = new ImageIcon(getClass().getResource("/geiza/voce/muito/amo/eu/img.jpg"));
            imgLabel.setIcon(icone);

        } else {
            imgLabel.setText("Obrigado pela preferencia volte mais vezes :D");
        }
    }//GEN-LAST:event_aceitarButtonActionPerformed

    private void recusarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recusarButtonActionPerformed
        if (nomeTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Insira seu nome por favor");
            return;
        }

        String nome = nomeTextField.getText();
        if (nome.contains("Geiza")) {
            JOptionPane.showMessageDialog(this, "Opção invalida, tente outra vez");
        } else {
            imgLabel.setText("Obrigado pela preferencia volte mais vezes :D");
        }
    }//GEN-LAST:event_recusarButtonActionPerformed

